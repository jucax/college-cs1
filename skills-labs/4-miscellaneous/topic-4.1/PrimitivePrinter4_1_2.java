class PrimitivePrinter4_1_2 {
    public static void print (byte x) {
        System.out.print("x is a byte");    
    }

    public static void print (short x) {
        System.out.print("x is a short");    
    }

    public static void print (int x) {
        System.out.print("x is an int");    
    }

    public static void print (long x) {
        System.out.print("x is a long");    
    }

    public static void print (float x) {
        System.out.print("x is a float");    
    }

    public static void print (double x) {
        System.out.print("x is a double");    
    }

    public static void print (char x) {
        System.out.print("x is a char");    
    }       
}